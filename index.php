          <h4>Departments</h4>
          <h5>Computer Science And Engineering</h5>
          <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Computer Science and Engineering encompasses a variety of topics that relates to computation, like analysis of algorithms, programming languages, program design, software, and computer hardware.Computer Science engineering has roots in electrical engineering, mathematics, and linguistics. In the past Computer Science was taught as part of mathematics or engineering departments & in the recent days it has emerged as a separate engineering field.</p>
          <h5>Civil Engineering</h5>
          <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Civil engineering is a professional engineering discipline that deals with the design, construction, and maintenance of the physical and naturally built environment, including works like roads, bridges, canals, dams, airports, sewerage systems, pipelines and railways.[1][2] Civil engineering is traditionally broken into a number of sub-disciplines. It is the second-oldest engineering discipline after military engineering,[3] and it is defined to distinguish non-military engineering from military engineering.[4] Civil engineering takes place in the public sector from municipal through to national governments, and in the private sector from individual homeowners through to international companies.</p>
          <h5>Mechanical Engineering</h5>
          <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Mechanical engineering is a professional programme which helps students to understand the working mechanisms of heavy tools and machineries.  The branch applies principles of engineering, physics and material science for the design, analysis, manufacturing and maintenance of mechanical systems. The course covers technical areas like distribution of electricity through generators, transformers, transmission lines, lighting and wiring in buildings. A course in mechanical engineering makes a student gain knowledge and experience in the designing of electric motors, automobiles, aircrafts and other heavy vehicles. Mechanical engineering demands knowledge about software used in technical work like computer-aided design and mathematical modelling.</p>
          <h5>Electrical Engineering</h5>
          <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Electrical engineering, one of the core courses of engineering discipline deals with the study of design, development, and maintenance of electrical systems and their components, ensuring quality, safety, reliability, and sustainability. The course focuses on the manufacturing of electrical equipment used in a number of sectors including construction and building and the production and distribution of power. Students pursuing electrical engineering study about semiconductors and microprocessors.</p>
          <h5>Mining Engineering</h5>
          <p>&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;Mining engineering is an engineering discipline that applies science and technology to the extraction of minerals from the earth. Mining engineering is associated with many other disciplines, such as geology, mineral processing and metallurgy, geotechnical engineering and surveying. A mining engineer may manage any phase of mining operations – from exploration and discovery of the mineral resource, through feasibility study, mine design, development of plans, production and operations to mine closure.</p>
